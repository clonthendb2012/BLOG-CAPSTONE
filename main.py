@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():

        if User.query.filter_by(email=form.email.data).first():
            # User already exists
            flash("¡Ya te registraste con ese correo electrónico, inicia sesión en su lugar!")
            return redirect(url_for('login'))

        hash_and_salted_password = generate_password_hash(form.password.data)
        new_user = User(
            email=form.email.data,
            password=hash_and_salted_password,
            name=form.name.data
        )
        db.session.add(new_user)
        db.session.commit()

        #This line will authenticate the user with Flask-Login
        login_user(new_user)
        print(login_user(new_user))
        print(current_user)

        return redirect(url_for('get_all_posts'))
    return render_template("register.html", form=form)


@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        email = form.email.data
        password = form.password.data

        user = User.query.filter_by(email=email).first()
        # Email doesn't exist
        if not user:
            flash("¡Email invalido intente de nuevo!")
            return redirect(url_for('login'))
        # Password incorrect
        elif not check_password_hash(user.password, password):
            flash("¡Password incorrecto, intente de nuevo!")
            return redirect(url_for('login'))
        else:
            login_user(user)
            print(current_user.name)
            return redirect(url_for('get_all_posts'))

    return render_template("login.html", form=form)


