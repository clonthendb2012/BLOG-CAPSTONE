def admin_only(f):
    @wraps(f)
    def decorated_function(*args, **kwargs):
        #Si la identificación no es 1, devuelva abortar con error 403
        if current_user.id != 1:
            return abort(403)
        #De lo contrario, continúe con la función de ruta
        return f(*args, **kwargs)
    return decorated_function


